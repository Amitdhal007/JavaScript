// while loop
// first we check condition then we execute body
const heros = ["thor", "captain", "ironamn", "hulk"]
let index = 0

while(index < heros.length) {
    console.log(heros[index]);
    index++
}


// do while loop
// first we execute then we check for condition

let x = 0

do {
    console.log(x);
    x++
} while (x <= 10)